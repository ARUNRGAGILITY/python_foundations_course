# Python doesn't have a built-in array type, but Python Lists can be used as arrays.
# They can store any type of data with dynamic sizing.

# Creating an array (list)
array = [1, 2, 3, 4, 5]

# Accessing elements
element = array[0]  # Access the first element (indexing starts at 0)

# Updating elements
array[1] = 10  # Update the second element to 10

print(array)  # Output: [1, 10, 3, 4, 5]
